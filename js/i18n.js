function normalizeLanguage(language) {
	const value = String(language || "").trim().toLowerCase();
	if (value.startsWith("en")) return "en";
	if (value.startsWith("es")) return "es";
	return DEFAULT_LANGUAGE;
}

function interpolate(template, params = {}) {
	return String(template).replace(/\{(\w+)\}/g, (_, key) => String(Object.prototype.hasOwnProperty.call(params, key) ? params[key] : ""));
}

export function getStoredLanguage() {
	try {
		return normalizeLanguage(globalThis.localStorage?.getItem(STORAGE_KEY) || globalThis.document?.documentElement?.lang || globalThis.navigator?.language);
	} catch {
		return DEFAULT_LANGUAGE;
	}
}

export function getCurrentLanguage() {
	return getStoredLanguage();
}

export function setStoredLanguage(language) {
	const normalized = normalizeLanguage(language);
	try {
		globalThis.localStorage?.setItem(STORAGE_KEY, normalized);
	} catch {
		// ignore storage failures
	}
	if (globalThis.document?.documentElement) {
		globalThis.document.documentElement.lang = normalized;
	}
	return normalized;
}

export function getLocaleForLanguage(language) {
	return {
		pt: "pt-PT",
		en: "en-GB",
		es: "es-ES",
	}[normalizeLanguage(language)] || "pt-PT";
}

export function getSupportedLanguages() {
	return ["pt", "en", "es"];
}

export function translate(language, key, params = {}) {
	const normalized = normalizeLanguage(language);
	const template = TRANSLATIONS[normalized]?.[key] ?? TRANSLATIONS.pt?.[key] ?? key;
	return interpolate(template, params);
}
